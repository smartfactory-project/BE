package com.example.demo.quality.model;

public class LineMonth {
    private String month;
    private int lineA;
    private int lineB;
    private int lineC;
    private int lineD;
    private int lineE;

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public int getLineA() {
        return lineA;
    }

    public void setLineA(int lineA) {
        this.lineA = lineA;
    }

    public int getLineB() {
        return lineB;
    }

    public void setLineB(int lineB) {
        this.lineB = lineB;
    }

    public int getLineC() {
        return lineC;
    }

    public void setLineC(int lineC) {
        this.lineC = lineC;
    }

    public int getLineD() {
        return lineD;
    }

    public void setLineD(int lineD) {
        this.lineD = lineD;
    }

    public int getLineE() {
        return lineE;
    }

    public void setLineE(int lineE) {
        this.lineE = lineE;
    }
}
